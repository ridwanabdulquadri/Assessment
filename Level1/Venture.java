package Level1;

public class Venture {
    public static void main(String[] args) {
        String[][] array = {{"VE"}, {"NT"}, {"UR"}, {"ES"}};
        for (int i = 0; i < array.length; i++) {
            System.out.print("");
            for (int j = 0; j < array[i].length; j++) {
                System.out.println(array[i][j]);
            }
        }
    }
}

